void main() {
  int a = 3;
  int b = 4;
// addition
  var c = a + b;
  print("The Sum of a and b is : ${c}");
  // multiplication
  var d = a * b;
  print("The Multiplication of a and b is : ${d}");
//subtraction
  var e = a - b;
  print("The Subtraction of a and b is : ${e}");
//division
  var f = a / b;
  print("The division of a and b is ${f}");
  // modulus
  var g = a % b;
  print("The reminder of a and b is ${g}");
}
