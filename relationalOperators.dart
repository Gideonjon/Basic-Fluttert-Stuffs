void main() {
  int a = 9;
  int b = 8;

  // Greater than
  var c = a > b;
  print("a is greater than b is ${c}");

  //less than
  var d = a < b;
  print("a is less than b is ${d}");

  //Greater than or equal to
  var e = a >= b;
  print("a is greater than or equal to b is ${e}");

  //equality
  var f = a == b;
  print("a is equal to b is ${f}");

  // not equal to
  var g = a != b;
  print("a is not equal to b is ${g}");
}
